import { Schema, model } from 'mongoose'

const usersSchema = new Schema({
  // id: Number,
  firstName: {
    type: String,
    required: true,
  },
  lastName: {
    type: String,
    required: true,
  },
  userName: {
    type: String,
    required: true,
    unique: true,
  },
  email: {
    type: String,
    required: true,
  },
  password: {
    type: String,
    required: true,
    unique: true,
  },
})

export const Users = model('Users', usersSchema)
